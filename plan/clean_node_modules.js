const fs = require('fs');
const path = require('path');

function deleteNodeModules(dir) {
  if (!fs.existsSync(dir)) return;
  fs.readdirSync(dir, { withFileTypes: true }).forEach(entry => {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      if (entry.name === 'node_modules') {
        console.log('Deleting:', fullPath);
        try {
          fs.rmSync(fullPath, { recursive: true, force: true });
        } catch (err) {
          console.error('Failed to delete', fullPath, err.message);
        }
      } else if (entry.name !== '.git') {
        deleteNodeModules(fullPath);
      }
    }
  });
}

console.log('Cleaning all node_modules...');
// Delete root node_modules first
const rootNodeModules = path.join(__dirname, '..', 'node_modules');
if (fs.existsSync(rootNodeModules)) {
  console.log('Deleting root:', rootNodeModules);
  try {
    fs.rmSync(rootNodeModules, { recursive: true, force: true });
  } catch (err) {
    console.error('Failed to delete root node_modules', err.message);
  }
}

deleteNodeModules(path.join(__dirname, '..'));
console.log('Clean complete.');
